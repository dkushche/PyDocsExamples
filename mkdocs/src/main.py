"""
Module of arithmetic operations
"""


def add(a: int, b: int) -> int:
    """
    Returns the sum of two integers.

    Args:
      a: The first integer.
      b: The second integer.

    Returns:
        The sum of a and b.
    """

    return a + b

if __name__ == '__main__':
    print(add(2, 2))
